public class Binary_Right_Shift {
    public static void main(String[] args) {
        /*
         * Binary Right Shift >>
         * Rules 
         * a >> b 
         * 6 >> 1  
         * A=000110
         *   _00011 --> 000011 = (3)
         * 
         * Formula for it's Calculation
         * a >> b = a/2^b
         * 6 >> 1 = 6/2^1 = 3
         */
        //Code
        System.out.println((6 >> 1)); // 3
    }
}
