public class JavaInfinitiveForLoop {
    /* If you use two semicolons ;; in the for loop it will be infinitive for loop 
     * Sysntax -
     *    for(;;){
     *    // code 
     *    }
    */
    public static void main(String[] args) {
        for(;;){
            System.out.println("this is an infinitive for loop");
        }
    }
}
